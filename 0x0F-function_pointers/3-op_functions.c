/**
 * op_add - Adds two arguments
 * @a: First argument
 * @b: Last argument
 *
 * Return: The sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Performs subtraction of two arguments
 * @a: First argument
 * @b: Last argument
 *
 * Return: b - a
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two arguments
 * @a: First argument
 * @b: Last argument
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Performs division of two arguments
 * @a: First argument
 * @b: Last argument
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Applies modulus operatio on two arguments
 * @a: First argument
 * @b: Last argument
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
