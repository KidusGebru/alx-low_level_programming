/**
 * add - Adds two operands
 * @a: Operand One
 * @b: Operand two
 *
 * Return: Result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtracts second operand from the first one
 * @a: First operand
 * @b: Second operand
 *
 * Return: Result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplies the two operands
 * @a: Operand one
 * @b: Operand two
 *
 * Return: Result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divides first operand by the second one
 * @a: Dividen
 * @b: Divider
 *
 * Return: Result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Modulo operation
 * @a: First operand
 * @b: Second operand
 *
 * Return: Result
 */
int mod(int a, int b)
{
	return (a % b);
}
